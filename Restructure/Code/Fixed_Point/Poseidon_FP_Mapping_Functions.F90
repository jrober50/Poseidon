PURE FUNCTION FP_Vector_Map( re, d )

INTEGER                                     :: FP_Vector_Map

INTEGER, INTENT(IN)                         :: re, d


FP_Vector_Map = re*DEGREE + d + 1


END FUNCTION FP_Vector_Map






