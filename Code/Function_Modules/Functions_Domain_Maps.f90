!+101+###########################################################################!
!                                                                                !
!                  FP_Vector_Map                                                  !
!                                                                                !
!################################################################################!
PURE FUNCTION Map_To_FEM_Node( re, d )

INTEGER                                     :: Map_To_FEM_Node

INTEGER, INTENT(IN)                         :: re, d


Map_To_FEM_Node = re*DEGREE + d + 1


END FUNCTION Map_To_FEM_Node






!+102+###########################################################################!
!                                                                                !
!                  FP_Vector_Map                                                  !
!                                                                                !
!################################################################################!
PURE FUNCTION Map_To_lm( l, m )

INTEGER                                     :: Map_To_lm

INTEGER, INTENT(IN)                         :: l, m


Map_To_lm = l*(l+1) + m + 1


END FUNCTION Map_To_lm




!+103+###########################################################################!
!                                                                                !
!                  FP_Vector_Map                                                  !
!                                                                                !
!################################################################################!
PURE FUNCTION Map_To_tpd( td, pd )

INTEGER                                     :: Map_To_tpd

INTEGER, INTENT(IN)                         :: td, pd


Map_To_tpd = (td-1)*Num_P_Quad_Points + pd


END FUNCTION Map_To_tpd




!+103+###########################################################################!
!                                                                                !
!                  FP_Vector_Map                                                  !
!                                                                                !
!################################################################################!
PURE FUNCTION Map_To_rtpd( rd, td, pd )

INTEGER                                     :: Map_To_rtpd

INTEGER, INTENT(IN)                         :: rd, td, pd


Map_To_rtpd = 0

END FUNCTION Map_To_rtpd




